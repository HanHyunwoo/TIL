package day1;
public class TypeTest4 {
	public static void main(String[] args) {
		char testMunja;
		testMunja = '°¡';
		System.out.print(testMunja);
		System.out.print(testMunja);
		testMunja = 'A';
		System.out.print(testMunja);
		testMunja = '%';
		System.out.print(testMunja);
		testMunja = '\n';
		System.out.print(testMunja);
		testMunja = '#';
		System.out.print(testMunja);
		testMunja = '\t';
		System.out.print(testMunja);
		testMunja = '9';
		System.out.print(testMunja);
	}
}
