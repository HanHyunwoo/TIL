package com.hw.vo;

public class Crime {
	String gu;
	String criminal;
	String state;
	int occurnum;
	public String getGu() {
		return gu;
	}
	public void setGu(String gu) {
		this.gu = gu;
	}
	public String getCriminal() {
		return criminal;
	}
	public void setCriminal(String criminal) {
		this.criminal = criminal;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public int getOccurnum() {
		return occurnum;
	}
	public void setOccurnum(int occurnum) {
		this.occurnum = occurnum;
	}
}
